public class PlusOneArrayLeetchallenge {
    public static void main(String[] args) {

        int[] digits = {8, 6, 5, 3, 1};
        for (int index = 0; index < digits.length ; index++) {
            for (int counter = index; counter <digits[4] ; counter++) {
                if (index==digits[4])
                    System.out.println(digits[4] +1 );

            }


        }

    }
}